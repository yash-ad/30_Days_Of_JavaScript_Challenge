// stringUtils.js should export two functions: toUpperCase(str) (returns the uppercase version of str) and toLowerCase(str) (returns the lowercase version of str).

//1:toUpperCase(str) (returns the uppercase version of str):-
export function toUpperCase(str) {
    return str.toUpperCase();
}
//2:toLowerCase(str) (returns the lowercase version of str):-
export function toLowerCase(str) {
    return str.toLowerCase();
}